package com.example.searchview;

public class SearchItem {
    int logo;
    String url;
    public SearchItem() {
    }
    public int getLogo() {
        return logo;
    }

    public void setLogo(int logo) {
        this.logo = logo;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}
